uint16_t crc16_update(uint16_t crc, uint8_t a) {
	uint8_t i;

	crc ^= a;
	for (i = 0; i < 8; i++) {
		if (crc & 1) crc = (crc >> 1) ^ 0xA001;
		else crc = (crc >> 1);
	}

	return crc;
}

